x="Hello Python"
print("string extract:",x)
print("string extract[4]:",x[4])
print("string extract[:]:",x[:])
print("string extract[0:4]:",x[0:4])
print("reverse string extract[:-1]:",x[:-1])
print("string length:",len(x))
print("string lowercase:",x.lower())
print("string uppercase:",x.upper())